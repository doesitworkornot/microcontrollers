int calc_pwm(double temperature, int max_pwm){
	double low = 26.0;
	double high = 40.0;
	double diff = high - low;
	double low_bound = (temperature <= low) ? 0: temperature - low;
	double upper_bound = (low_bound >= diff) ? diff : low_bound;
	double scale = upper_bound / diff;
	int pwm = (int)(max_pwm * scale);
	return pwm;
}

int cos_by_time(int max_pwm){
	double result = (cos(ms) + 1.0) / 2.0;
	return (int)(max_pwm * result);
}

